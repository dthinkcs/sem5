	MOV R5,#08
	LDR R4,=04; COUNTER FOR THE LEFT SHIFTING

UP2 LDRB R1,[R0],#-1
    LSL R9,R4
	ADD R9,R9,R1
	;ADD R4,#04
	SUB R5,#01
	TEQ R5,#0
	BNE UP2

STOP B STOP
